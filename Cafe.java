public class Cafe {
    
    private String nombre;
    private int gCafe;
    private int gCacao;
    private int gLeche;

    public Cafe (String nombre, int gCafe, int gCacao, int gLeche){
        this.nombre = nombre;
        this.gCafe = gCafe;
        this.gCacao = gCacao;
        this.gLeche = gLeche;
    }

    
    
}
